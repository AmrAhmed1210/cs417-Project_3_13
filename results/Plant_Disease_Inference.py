def predict_image(image_path):
    if not os.path.exists(image_path):
        print(f"Error: Image not found at {image_path}")
        return

    img = image.load_img(image_path, target_size=(128, 128))
    x = image.img_to_array(img)
    x = np.expand_dims(x, axis=0)
    x = x / 255.0

    predictions = model.predict(x)
    predicted_class_index = np.argmax(predictions)
    predicted_class_name = class_names[predicted_class_index]
    confidence = np.max(predictions) * 100

    print("\n" + "=" * 30)
    print(f"Result: {predicted_class_name}")
    print(f"Confidence: {confidence:.2f}%")
    print("=" * 30 + "\n")

    plt.imshow(image.load_img(image_path))
    plt.title(f"{predicted_class_name} ({confidence:.1f}%)")
    plt.axis('off')
    plt.show()

import ipywidgets as widgets
from IPython.display import display, clear_output

text_input = widgets.Text(
    value='',
    placeholder='e.g., download (1).jfif',
    description='Image Name:',
    disabled=False
)

button = widgets.Button(
    description='Predict Disease',
    button_style='success',
    icon='search'
)

output_area = widgets.Output()

def on_button_click(b):
    with output_area:
        clear_output()

        img_path = text_input.value.replace('"', '').strip()

        if img_path:
            predict_image(img_path)
        else:
            print("Please enter an image name first.")

button.on_click(on_button_click)
print("\n PLANT DISEASE DEMO TOOL")
display(text_input, button, output_area)